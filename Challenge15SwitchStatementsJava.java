// Switch Statements Challenge
public class SwitchChallenge{
	public static void main(String []args){
			
		char switchExpression = 'A';
		
		switch(switchExpression){
			case 'A':
					System.out.println("it was A");
					break;
					
			case 'B':
					System.out.println("it was B");
					break;
					
			case 'C':
					System.out.println("it was C");
					break;
					
			case 'D':
					System.out.println("it was D");
					break;
					
			case 'E':
					System.out.println("it was E");
					break;
					
			default:
					System.out.println("Not A, B, C, D and E");
		}
	}
}